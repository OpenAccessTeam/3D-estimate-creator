<template>
    <div class="objectItem" v-on:click="clickedObjectItem">
        <img class="objectItemIcon" :src="`assets/objectIcons/${ name }.jpg`"/>
        <span class="objectItemTitle">{{ name }}</span>
    </div>
</template>

<script>
    import {actionCreator, ADD_OBJECT_DISPLAYED, SHOW_DETAILS_PANEL} from '../../../actions';

    export default {
        name: "object-item",
        methods: {
            clickedObjectItem: function () {
                const generateUid = () => 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(
                    /[xy]/g, 
                    function (c) {
                        const r = Math.random() * 16 | 0, v = c === 'x' ? r : (r & 0x3 | 0x8);
                        return v.toString(16);
                    });

                const uid = generateUid();

                this.$root.$emit('put', actionCreator(SHOW_DETAILS_PANEL, {
                    itemName: this.name,
                    item: this.item,
                    uid
                }));

                this.$root.$emit('put', actionCreator(ADD_OBJECT_DISPLAYED, {
                    itemName: this.name,
                    item: this.item,
                    uid
                }));
            }
        },
        props: ['name', 'item']
    }
</script>

<style lang=css>
    .objectItem {
        width: 9vw;
        height: 9vw;
        overflow: hidden;
        text-align: center;
    }

    .objectItemIcon {
        width: 7vw;
        height: 7vw;
    }

    .objectItemTitle {
        color: black;
        word-break: none;
    }
</style>